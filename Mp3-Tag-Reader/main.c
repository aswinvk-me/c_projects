#include "mp3_header.h"
int main(int argc, char *argv[])
{
	//if no arguments found
	if(argc < 2)
	{
		printf("#########################################################################\n");
		printf("=========================================================================\n");
		printf("                     Error: no arguments found!!\n");
		printf("-------------------------------------------------------------------------\n");
		help_mp3();
		return 0;
	}
	//view function
	if(strcmp(argv[1], "-v") == 0)
	{
		printf("#########################################################################\n");
		printf("=========================================================================\n");
		printf("              ||        MP3 TAG [READER] AND EDITOR     ||       ID3 v2.3\n");
		printf("=========================================================================\n");
		view_mp3(argv[2]);
	}
	//edit function
	else if(strcmp(argv[1], "-e") == 0)
	{
		printf("#########################################################################\n");
		printf("=========================================================================\n");
		printf("              ||        MP3 TAG READER AND [EDITOR]     ||       ID3 v2.3\n");    
		printf("=========================================================================\n");
		edit_mp3(argv[2], argv[3], argv[4]);
	}
	//help function
	else if(strcmp(argv[1], "--help") == 0)
	{
		printf("#########################################################################\n");
		printf("=========================================================================\n");
		printf("    [USAGE]   ||        MP3 TAG READER AND EDITOR       ||       ID3 v2.3\n");
		printf("=========================================================================\n");
		help_mp3();
	}
	// if not correct arguments 
	else
	{
		printf("#########################################################################\n");
		printf("=========================================================================\n");
		printf("                     Error: Invalid argument!!\n");
		printf("-------------------------------------------------------------------------\n");
		help_mp3();
		return 0;
	}
		
}
